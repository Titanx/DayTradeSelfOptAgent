"""Skill document loader — reads .skill.md files and converts to system prompts.

SkillOpt 架构: 将 Agent prompt 从 Python 代码中剥离为 Markdown skill 文档.
如果 skill 文件不存在，自动回退到硬编码的 LEGACY 版本。
"""

import re
from pathlib import Path
from typing import Optional

PROJECT_DIR = Path(__file__).parent.parent
SKILLS_DIR = PROJECT_DIR / "skills"


def _md_to_prompt(md_text: str) -> str:
    """将 SKILLOPT-EDITABLE 区域的 rule/anti 行提取为简洁 prompt。

    保持前端元数据(stage/strategy)和标记<!-- SKILLOPT-EDITABLE -->不变,
    以便 Optimizer LLM 后续编辑。
    """
    return md_text


def load_skill(name: str) -> Optional[str]:
    """加载 skill 文档。

    Args:
        name: skill 名称 (如 "bull_researcher", "trader")

    Returns:
        skill 文档全文，或 None (文件不存在时)
    """
    skill_path = SKILLS_DIR / f"{name}.skill.md"
    if skill_path.exists():
        return skill_path.read_text(encoding="utf-8")
    return None


def get_system_prompt(name: str) -> str:
    """获取 Agent 的 system prompt。

    优先读取 skills/{name}.skill.md，不存在时回退到 LEGACY 硬编码版本。
    """
    skill = load_skill(name)
    if skill:
        return skill
    return _LEGACY_FALLBACK.get(name, f"[MISSING PROMPT: {name}]")


